def product_inline_kb(product_id: int) -> InlineKeyboardMarkup:
    kb = InlineKeyboardMarkup()
    kb.add(InlineKeyboardButton(text="‚ûï –î–æ–¥–∞—Ç–∏ —É –∫–æ—à–∏–∫", callback_data=f"add:{product_id}"))
    kb.add(InlineKeyboardButton(text="üõí –í—ñ–¥–∫—Ä–∏—Ç–∏ –∫–æ—à–∏–∫", callback_data="cart:open"))
    return kb

def cart_inline_kb(items: list) -> InlineKeyboardMarkup:
    kb = InlineKeyboardMarkup()
    for it in items:
        kb.add(InlineKeyboardButton(
            text=f"‚ùå {it['name']} (‚Äì1)",
            callback_data=f"cart:remove:{it['product_id']}"
        ))
    if items:
        kb.add(InlineKeyboardButton(text="üßπ –û—á–∏—Å—Ç–∏—Ç–∏ –≤—Å–µ", callback_data="cart:clear"))
